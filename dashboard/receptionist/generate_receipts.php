<?php
require_once('../../includes/session_check.php');
require_once('../../includes/db_connect.php');

// Get receptionist ID from session
$receptionist_id = $_SESSION['user_id'] ?? null;

if (!$receptionist_id) {
    header("Location: /GYM_MANAGEMENT_SYSTEM/login.php");
    exit();
}

// Initialize variables
$error = '';
$success = '';

// Handle form submission
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $member_id = $_POST['member_id'] ?? '';
    $plan_id = $_POST['plan_id'] ?? '';
    $amount_paid = $_POST['amount_paid'] ?? '';
    $date_of_purchase = $_POST['date_of_purchase'] ?? '';
    $date_of_creation = date('Y-m-d');

    // Validate inputs
    if (empty($member_id) || empty($plan_id) || empty($amount_paid) || empty($date_of_purchase)) {
        $error = "All fields are required";
    } else {
        $stmt = $conn->prepare("INSERT INTO receipt (Member_id, Generated_by, Plan_id, Amount_paid, Date_of_creation, Date_of_purchase)
                                VALUES (?, ?, ?, ?, ?, ?)");
        $stmt->bind_param("iiidss", $member_id, $receptionist_id, $plan_id, $amount_paid, $date_of_creation, $date_of_purchase);

        if ($stmt->execute()) {
            $success = "Receipt generated successfully!";
        } else {
            $error = "Failed to generate receipt: " . $stmt->error;
        }
        $stmt->close();
    }
}

// Fetch active members
$member_query = $conn->prepare("SELECT ID, Name FROM User WHERE User_type = 'Member' ORDER BY Name");
$member_query->execute();
$member_result = $member_query->get_result();

// Fetch active plans
$plan_query = $conn->prepare("SELECT Plan_id, Name, Amount FROM membership_plan WHERE Active = 1 ORDER BY Name");
$plan_query->execute();
$plan_result = $plan_query->get_result();

// Fetch receipts generated by this receptionist
$receipt_query = $conn->prepare("
    SELECT r.Receipt_no, u.Name AS Member_Name, mp.Name AS Plan_Name, r.Amount_paid, r.Date_of_purchase, r.Date_of_creation
    FROM receipt r
    JOIN User u ON r.Member_id = u.ID
    JOIN membership_plan mp ON r.Plan_id = mp.Plan_id
    WHERE r.Generated_by = ?
    ORDER BY r.Date_of_creation DESC
");
$receipt_query->bind_param("i", $receptionist_id);
$receipt_query->execute();
$receipt_result = $receipt_query->get_result();

// Check for preselected member from registration
$preselected_member = $_GET['member_id'] ?? null;
?>

