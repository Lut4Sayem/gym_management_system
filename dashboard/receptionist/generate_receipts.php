        }
        .error {
            color: red;
            margin: 10px 0;
        }
        .success {
            color: green;
            margin: 10px 0;
        }
    </style>
    <script>
        function updateAmount() {
            const select = document.getElementById("plan_id");
            const selectedOption = select.options[select.selectedIndex];
            const amount = selectedOption.getAttribute("data-amount");
            document.getElementById("amount_paid").value = amount;
        }
        
        // Auto-select member if coming from registration
        document.addEventListener('DOMContentLoaded', function() {
            <?php if ($preselected_member): ?>
                const memberSelect = document.querySelector('select[name="member_id"]');
                if (memberSelect) {
                    memberSelect.value = <?php echo json_encode($preselected_member); ?>;
                }
            <?php endif; ?>
        });
    </script>
</head>
<body>

<h2>Generate Receipt</h2>

<?php if (!empty($error)): ?>
    <div class="error"><?php echo htmlspecialchars($error); ?></div>
<?php endif; ?>

<?php if (!empty($success)): ?>
    <div class="success"><?php echo htmlspecialchars($success); ?></div>
<?php endif; ?>

<form method="POST">
    <label for="member_id">Select Member:</label>
    <select name="member_id" required>
        <option value="">-- Select Member --</option>
        <?php while ($row = $member_result->fetch_assoc()): ?>
            <option value="<?= $row['ID'] ?>" <?= ($preselected_member == $row['ID']) ? 'selected' : '' ?>>
                <?= htmlspecialchars($row['Name']) ?> (ID: <?= $row['ID'] ?>)
            </option>
        <?php endwhile; ?>
    </select>

    <label for="plan_id">Select Membership Plan:</label>
    <select name="plan_id" id="plan_id" onchange="updateAmount()" required>
        <option value="">-- Select Plan --</option>
        <?php while ($row = $plan_result->fetch_assoc()): ?>
            <option value="<?= $row['Plan_id'] ?>" data-amount="<?= $row['Amount'] ?>">
                <?= htmlspecialchars($row['Name']) ?> (৳<?= $row['Amount'] ?>)
            </option>
        <?php endwhile; ?>
    </select>

    <label for="amount_paid">Amount Paid (৳):</label>
    <input type="number" step="0.01" name="amount_paid" id="amount_paid" required>

    <label for="date_of_purchase">Date of Purchase:</label>
    <input type="date" name="date_of_purchase" value="<?= date('Y-m-d') ?>" required>

    <button type="submit">Generate Receipt</button>
</form>

<!-- Previously Generated Receipts -->
